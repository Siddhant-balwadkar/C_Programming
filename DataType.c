#include<stdio.h>

int main()
{
    char cValue = 's';
    int iValue = 11;
    float fValue = 90.78f;
    double dValue = 98.564323;

    printf("%c\n",cValue);  //s 
    printf("%d\n",iValue);  //11
    printf("%f\n",fValue);  //90.78
    printf("%lf\n",dValue);  //98.564323

    return 0;
}